package com.enovlab.yoop.data.entity.enums

enum class NotificationType {

    // General
    USER_EMAIL_VERIFICATION_OR_RESET_PASSWORD,
    USER_WELCOME,
    USER_PAYMENT_METHOD_CHANGED,
    USER_PASSWORD_CHANGED,
    USER_EMAIL_VERIFICATION_RESET_PASSWORD,
    USER_MOBILE_NUMBER_VERIFICATION,
    PRIVACY_POLICY_UPDATE,

    // List
    USER_DRAW_WON,
    USER_DRAW_INITIAL_ENTRY,
    USER_DRAW_TOKEN_COUNT_UPDATE,
    USER_DRAW_DELETE_ENTRY,
    USER_DRAW_LOST_PAYMENT_DECLINED,
    USER_DRAW_LOST_NOT_CHOSEN,
    USER_DRAW_LOST_PAYMENT_FAILURE,
    USER_DRAW_LOST_CLAIM_TIME_EXPIRED,

    // On-Sale
    USER_AUCTION_WON,
    USER_AUCTION_INITIAL_BID_CONFIRMATION,
    USER_AUCTION_BID_UPDATED,
    USER_AUCTION_OFFER_CHANCE_CHANGE,
    USER_AUCTION_DELETE_BID,
    USER_AUCTION_ENDING_SOON,
    USER_AUCTION_LOST_OUTBID,
    USER_AUCTION_LOST_PAYMENT_FAILURE,
    USER_BUY_NOW_CONFIRMATION,

    // Secured
    USER_DRAW_WON_PAYMENT_SUCCESS,
    USER_DRAW_WON_INVENTORY_ALLOCATED,
    USER_AUCTION_WON_PAYMENT_SUCCESS,
    USER_AUCTION_INVENTORY_ALLOCATED,

    // Action required
    USER_DRAW_WON_NEEDS_CLAIM,
    USER_DRAW_WON_PAYMENT_FAILED,
    USER_AUCTION_WON_PAYMENT_FAILED,

    // Token assignment
    USER_TOKEN_ASSIGNMENT_INVITE_SENT,
    USER_TOKEN_ASSIGNMENT_INVITE_ACCEPTED,
    USER_TOKEN_ASSIGNMENT_INVITE_DECLINED,
    USER_TOKEN_ASSIGNMENT_REVOKE_INVITEE_CONFIRMATION,
    USER_TOKEN_ASSIGNMENT_INVITEE_CANCEL,
    INVITEE_TOKEN_ASSIGNMENT_INVITE,
    INVITEE_TOKEN_ASSIGNMENT_ACCEPTED,
    INVITEE_TOKEN_ASSIGNMENT_DECLINED,
    INVITEE_TOKEN_ASSIGNMENT_CANCEL,
    INVITEE_TOKEN_ASSIGNMENT_OWNER_TOKEN_RECLAIM,
    INVITEE_TOKEN_ASSIGNMENT_OWNER_INVITE_RECLAIM,

    // Reminder
    USER_EVENT_REMINDER_EVENT_READY,
    USER_EVENT_REMINDER_ASSIGNMENTS_REQUIRED,
    USER_EVENT_REMINDER_PREPARE,
    INVITEE_TOKEN_ASSIGNMENT_INVITE_REMINDER,
    INVITEE_TOKEN_ASSIGNMENT_EVENT_READY_REMINDER;

    fun isList() = this == USER_DRAW_WON || this == USER_DRAW_INITIAL_ENTRY || this == USER_DRAW_TOKEN_COUNT_UPDATE
        || this == USER_DRAW_DELETE_ENTRY || this == USER_DRAW_LOST_PAYMENT_DECLINED
        || this == USER_DRAW_LOST_NOT_CHOSEN || this == USER_DRAW_LOST_PAYMENT_FAILURE
        || this == USER_DRAW_LOST_CLAIM_TIME_EXPIRED

    fun isOnSale() = this == USER_AUCTION_WON || this == USER_AUCTION_INITIAL_BID_CONFIRMATION || this == USER_AUCTION_BID_UPDATED
        || this == USER_AUCTION_OFFER_CHANCE_CHANGE || this == USER_AUCTION_DELETE_BID
        || this == USER_AUCTION_ENDING_SOON || this == USER_AUCTION_LOST_OUTBID
        || this == USER_AUCTION_LOST_PAYMENT_FAILURE || this == USER_BUY_NOW_CONFIRMATION

    fun isSecured() = this == USER_DRAW_WON_PAYMENT_SUCCESS || this == USER_DRAW_WON_INVENTORY_ALLOCATED
        || this == USER_AUCTION_WON_PAYMENT_SUCCESS || this == USER_AUCTION_INVENTORY_ALLOCATED

    fun isActionRequired() = this == USER_DRAW_WON_NEEDS_CLAIM || this == USER_DRAW_WON_PAYMENT_FAILED
        || this == USER_AUCTION_WON_PAYMENT_FAILED

    fun isAssignment() = this == USER_TOKEN_ASSIGNMENT_INVITE_SENT || this == USER_TOKEN_ASSIGNMENT_INVITE_ACCEPTED
        || this == USER_TOKEN_ASSIGNMENT_INVITE_DECLINED || this == USER_TOKEN_ASSIGNMENT_REVOKE_INVITEE_CONFIRMATION
        || this == USER_TOKEN_ASSIGNMENT_INVITEE_CANCEL || this == INVITEE_TOKEN_ASSIGNMENT_INVITE
        || this == INVITEE_TOKEN_ASSIGNMENT_ACCEPTED || this == INVITEE_TOKEN_ASSIGNMENT_DECLINED
        || this == INVITEE_TOKEN_ASSIGNMENT_CANCEL || this == INVITEE_TOKEN_ASSIGNMENT_OWNER_TOKEN_RECLAIM
        || this == INVITEE_TOKEN_ASSIGNMENT_OWNER_INVITE_RECLAIM

    fun isReminder() = this == USER_EVENT_REMINDER_EVENT_READY || this == USER_EVENT_REMINDER_ASSIGNMENTS_REQUIRED
        || this == USER_EVENT_REMINDER_PREPARE || this == INVITEE_TOKEN_ASSIGNMENT_INVITE_REMINDER
        || this == INVITEE_TOKEN_ASSIGNMENT_EVENT_READY_REMINDER

    fun isGeneral() = !isList() && !isOnSale() && !isSecured() && !isActionRequired() && !isAssignment() && !isReminder()
}